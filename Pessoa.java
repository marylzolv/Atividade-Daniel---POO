public class Pessoa{
   //atributos
    
   private String nome;
   private int idade;
   private String endereco;

   //metodos

   public void setNome(String nome){
       this.nome =nome;
}

public String getNome() {
    return nome;
}

public int getIdade() {
    return idade;
}

public void setIdade(int idade) {
    this.idade = idade;
}

public String getEndereco() {
    return endereco;
}

public void setEndereco(String endereco) {
    this.endereco = endereco;
}

}
